    <h3>Magyar Nemzeti Galéria Adattári Osztály</h3>
    <h4 class="mb-4">Képző-és Iparművészeti Lektorátus archívum</h4>

    <p>A Magyar Nemzeti Galéria Adattári Osztálya 2014-ben a Képző-és Iparművészeti Lektorátus archív anyagait tartalmazó önálló dokumentációs egységgel bővült. A lektorátusi archívum számos egyedülálló írásos és képi dokumentumot tartalmaz, melyek adalékul szolgálhatnak a Kádár-éra kultúrpolitikai irányelveinek megértéséhez a cenzori jogosultságokkal bíró állami szerv széleskörű tevékenységén keresztül.</p>

    <p>A Lektorátus kiemelt feladata volt a beruházásokkal, valamint a közterületen elhelyezésre kerülő képző-és iparművészeti alkotásokkal összefüggő feladatok koordinálása és lebonyolítása. Az archívum "köztéri" irategysége tartalmazza az egyes megbízások ügyiratait – sikeres példa esetében: a műalkotás igényének bejelentésétől a kész mű átvételéig. Az egyes köztéri mappákban tehát adatokat találhatunk többek között a mű igényének tematikai indoklására, a bíráló bizottság szakmai értékelésére (pl. az elfogadás, módosítás, elutasítás okaira), az elhelyezés körülményeire és az alkotást jellemző paraméretekre vonatkozóan.</p>

    <p>A kutatást segíti továbbá az egyes megbízásokat (a tervfázisokat és/vagy a kész művet) illusztráló köztéri fotótár és egy meta adatokat tartalmazó számítógépes adatbázis.</p>

    <p class="font-weight-bold">Az Adattári Osztály átszervezés és költözés miatt nem látogatható, ezért a kutatószolgálat sem üzemel. A Köztérkép közösségének e-mailben, vagy telefonon érkező kéréseivel az archívumot kezelő kolléga heti egy napon (szerdánként 10-18 óráig, az ünnepnapok és szabadságnapok kivételével) tud foglalkozni (erre a telefonos kutatási kérések alkalmával legyenek tekintettel). A beérkezett kérések mennyiségének függvényében a kérés teljesítése min. 1 max. 3 hetet vehet igénybe.</p>

    <p>A kutatási kérések a zárva tartás ideje alatt a következőkre vonatkozhatnak:</p>

    <ul>
      <li>alkotó köztéri/közösségi célzatú munkásságára vonatkozó meta adatok (helyszín, dátumok, társszerzők, méret-anyag-technika stb.) megosztása</li>
      <li>intézmény és helyszín alapján azonosított munkák meta adatainak megosztása (a kereső erre kevésbé optimalizált)</li>
      <li>a Budapesten elhelyezett alkotások kataszteri gyűjteményének megosztása (kerületekre bontva érhető el)</li>
    </ul>

