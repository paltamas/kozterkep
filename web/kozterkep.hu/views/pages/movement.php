    <nolink class="lead font-weight-bold">A Köztérkép Mozgalmat 2011-ben alapítottuk. A célunk az volt, hogy egy picit komolyabban vegyük azt, amit az elmúlt években létrehoztunk. A mozgalom a www.kozterkep.hu weboldalhoz (továbbiakban: weboldal) kapcsolódóan működik.</nolink>

    <h4 class="subtitle mt-5">A Mozgalom alapvetése</h4>
    <p>A Mozgalom a Köztérkép gyűjteményes adatbázisának tulajdonosa.</p>
    <p>Alapvető feladatunknak tekintjük a már felderített, és a weboldalon bemutatott köztéri és közösségi terekben fellelhető kulturális alkotások megőrzésének támogatását.</p>

    <h4 class="subtitle mt-5">A Mozgalom tagsága</h4>
    <p>A Mozgalomnak bárki tagja lehet, aki elfogadja jelen alapszábályunkat. A weboldalra regisztált minden tag regisztrációjával automatikusan a Mozgalom tagjává válik.</p>
    <p>A tagoknak joga van a szavazásra bocsátott kérdésekben szavazni. A tagsággal semmilyen kötelezettség nem jár, tagdíj nincs és nem is lesz.</p>

    <h4 class="subtitle mt-5">A Mozgalom tevékenysége</h4>
    <p>A hazai és magyar vonatkozású, egyedi értéket képviselő művészeti alkotások gyűjtése, saját fényképekkel és leírással történő bemutatása a Mozgalom weboldalán.</p>
    <p>A megosztott ismeretanyag folyamatos ápolása, korrigálása és kiegészítése új fényképekkel és információkkal. A közösség tagjainak kölcsönös támogatása, összehangolt csapatmunkájának biztosítása.</p>
    <p>Felület biztosítása azok számára, akik a köztereken és a közösségi tereken fellelhető alkotások létrehozásában vesznek részt.</p>
    <p>Újdonságok és hírek megosztása új alkotások felállításáról és más kapcsolódó kulturális eseményekről.</p>
    <p>Az alkotások állagmegóvásával, különös veszélyeztetettségével vagy rongálásával kapcsolatos információk soron kívüli közreadása és ezen alkotások fokozott kiemelése.</p>
    <p>Az erre érdemes és veszélyben lévő alkotások védelmének biztosítása hivatalos védettségre jelölésen keresztül.</p>
    <p>Rendezvények szervezése, ahol a szakmailag érintett és a laikus pártolók találkozhatnak egymással.</p>

    <h4 class="subtitle mt-5">Működés, szervezeti felépítés</h4>
    <p>A Mozgalom nem bejegyzett szervezetként, mint internetes mozgalom működik, tevékenységét a weboldal Üzemgazdája koordinálja.</p>
